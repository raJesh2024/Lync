package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {
		
		//Local Variables
		byte b = 10;
		short s = 20;
		int i = 30;
		long l = 40l;
		
		boolean bb = true;
		
		double d = 10.0d;
		float f = 20.0f;
		
		char ch = 'A';
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		
		System.out.println(bb);
		System.out.println(d);
		System.out.println(f);
		
		System.out.println(ch);
	}
}
