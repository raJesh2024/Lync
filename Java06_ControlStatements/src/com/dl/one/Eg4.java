package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {

		int userSalary = 20000;

		switch (userSalary) {
		case 10000:

			System.out.println("User Salary is " + userSalary);
			break;
		case 20000:

			System.out.println("User Salary is " + userSalary);
			break;
		case 30000:

			System.out.println("User Salary is " + userSalary);
			break;
		case 40000:

			System.out.println("User Salary is " + userSalary);
			break;

		default:
			System.out.println("Salary is out of range");
			break;
		}

	}

}
