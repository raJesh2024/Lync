package com.dl.two;

public class Eg3 {

	// do while
	// if we want to execute the iteration only one time
	public static void main(String[] args) {
		
		int i = 0;
		do {
			System.out.println(i);
			i++;
		} while (i<=5);
		
		int j = 5;
		do {
			System.out.println(j);
			j--;
		} while (j>=0);
	}

}
