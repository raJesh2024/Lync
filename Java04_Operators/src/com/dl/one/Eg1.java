package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		
		//Arithmetic Operators
		int a = 10;
		int b = 20;
		System.out.println(a + b); // 30
		System.out.println(a - b); // -10
		System.out.println(a * b); // 20 
		
		a = 15;
		b = 3;
		System.out.println(a / b); // 5
		System.out.println(a % b); // 0

		
	}

}
