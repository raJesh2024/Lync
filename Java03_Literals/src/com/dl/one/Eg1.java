package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		
		//Integer Literals
		int a = 10;
		//int is data type
		// a is variable
		// = is operation 
		//10 constant value
		
		//Floating Point
		float f1 = 10.0f;
		System.out.println(f1);
		
		double d1 = 20.0d;
		System.out.println(d1);
		
		//String Literals
		String username = "sai kiran";
		System.out.println(username);
		
		//Boolean Literals
		boolean b1 = true;
		System.out.println(b1);
		
		//Character Literals
		char ch1 = 'A';
		System.out.println(ch1);
		
		char ch2 = 97;
		System.out.println(ch2);
		
		char ch3 = '\u0000';
		System.out.println(ch3);

	}
	

}
